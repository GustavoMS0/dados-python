"""
    - soma: +
    - subtração: - 
    - multiplicação: *
    - divisão: /
    - divisão inteira: // ignora tudo após a virgula
    - potencia: ** Numero elevado a x  ex: "3" ** "x"
"""

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 % 3)
print(2 ** 3)